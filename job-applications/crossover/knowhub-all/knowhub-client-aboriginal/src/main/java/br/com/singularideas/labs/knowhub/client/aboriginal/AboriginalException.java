package br.com.singularideas.labs.knowhub.client.aboriginal;

public class AboriginalException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public AboriginalException() {
		super();
	}

	public AboriginalException(String message, Throwable cause) {
		super(message, cause);
	}

	public AboriginalException(String message) {
		super(message);
	}

	public AboriginalException(Throwable cause) {
		super(cause);
	}

}
