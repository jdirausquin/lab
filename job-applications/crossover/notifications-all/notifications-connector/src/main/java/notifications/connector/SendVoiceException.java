package notifications.connector;


/**
 * @author luismr
 *
 */
public class SendVoiceException extends NotificationConnectorException {

	private static final long serialVersionUID = 1L;

	public SendVoiceException() {
		super();
	}

	public SendVoiceException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public SendVoiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public SendVoiceException(String message) {
		super(message);
	}

	public SendVoiceException(Throwable cause) {
		super(cause);
	}

	
	
}
